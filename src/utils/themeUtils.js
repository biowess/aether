/**
 * Select the best matching preset for current conditions.
 * Automatically pulls from LIGHT_PRESETS when light mode is active.
 */
export function selectThemePreset(timeOfDay, weatherGroup) {
  // Cache for re-use when scheme changes
  _lastTimeOfDay    = timeOfDay;
  _lastWeatherGroup = weatherGroup;

  const map = _scheme === 'light' ? LIGHT_PRESETS : DARK_PRESETS;
  const key = `${timeOfDay}_${weatherGroup}`;
  if (map[key]) return map[key];

  const timeKey = `${timeOfDay}_clear`;
  if (map[timeKey]) return map[timeKey];

  return _scheme === 'light' ? LIGHT_PRESETS.day_clear : DARK_PRESETS.night_clear;
}

/**
 * Apply a theme preset to :root as inline CSS variables.
 */
export function applyTheme(preset) {
  const root = document.documentElement;
  Object.entries(preset).forEach(([key, value]) => {
    root.style.setProperty(key, value);
  });
}

/**
 * Apply a named color scheme (light / dark / system).
 * Also re-applies the last atmospheric theme so the scheme change
 * is instant — no weather reload required.
 */
export function applyColorScheme(scheme) {
  const root = document.documentElement;

  // Resolve 'system' to actual dark/light
  if (scheme === 'system') {
    _scheme = window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
  } else {
    _scheme = scheme; // 'dark' | 'light'
  }

  // Set or remove the data-theme attribute (used by high-contrast CSS overrides)
  if (_scheme === 'light') {
    root.setAttribute('data-theme', 'light');
  } else {
    root.removeAttribute('data-theme');
  }

  // Immediately re-apply atmospheric colors for the new scheme,
  // so the toggle is instant without waiting for a weather refresh.
  if (_lastTimeOfDay && _lastWeatherGroup) {
    const preset = selectThemePreset(_lastTimeOfDay, _lastWeatherGroup);
    applyTheme(preset);
  }
}